<?php
declare(strict_types=1);

/**
 * Серверная нормализация названий упражнений (HANDOFF §5, шаг 6).
 * Зеркало CANON / normSession из src/data.js. Применяется при сейве
 * (POST /sessions), при импорте (POST /import) и при сидинге (db/seed.php),
 * чтобы дубли синонимов не попадали в БД независимо от источника.
 *
 * При правке списка синонимов держать в синхроне с src/data.js.
 */
final class Canon
{
    /** Синоним -> канон. */
    public const MAP = [
        'Жим гантелей лёжа'                  => 'Жим гантелей 15°',
        'Жим гантелей под углом 30°'         => 'Жим гантелей 15°',
        'Жим лёжа'                           => 'Жим штанги лёжа',
        'Жим в тренажёре на грудь (1 рука)'  => 'Жим в тренажёре сидя на грудь',
        'Махи в стороны'                     => 'Махи гантелей в стороны',
        'Молотковые'                         => 'Молотковые подъёмы гантелей',
        'Бицепс штанга'                      => 'Подъём штанги на бицепс',
        'Отжимания на брусьях'               => 'Брусья',
        'Подтягивания в гравитроне'          => 'Подтягивания',
        'Подъёмы на носки в тренажёре сидя'  => 'Подъём на носки в тренажёре сидя',
        'Подъём ног в висе или dead bug'     => 'Подъём ног в висе',
        'Протяжка к подбородку'              => 'Протяжка штанги к подбородку',
        'Разведения лёжа'                    => 'Разведения гантелей лёжа',
        'Бабочка / сведения'                 => 'Сведения в тренажёре',
        'Сведения в тренажёре бабочка'       => 'Сведения в тренажёре',
        'Сведения в кроссовере'              => 'Сведения в тренажёре',
        'Трицепс в блоке / канат'            => 'Трицепс в канате',
    ];

    /** Канонизировать одно название. */
    public static function name(string $n): string
    {
        return self::MAP[$n] ?? $n;
    }

    /**
     * Нормализовать массив упражнений сессии: канонизировать названия и
     * склеить подходы упражнений с одинаковым каноном (порядок первого вхождения).
     *
     * @param array $exercises  [ ['n' => string, 'sets' => array], ... ]
     * @return array
     */
    public static function exercises(array $exercises): array
    {
        $merged = [];          // canon => index в $out
        $out = [];
        foreach ($exercises as $e) {
            if (!is_array($e)) continue;
            $n = self::name((string) ($e['n'] ?? ''));
            $sets = isset($e['sets']) && is_array($e['sets']) ? array_values($e['sets']) : [];
            if (isset($merged[$n])) {
                $out[$merged[$n]]['sets'] = array_merge($out[$merged[$n]]['sets'], $sets);
            } else {
                $merged[$n] = count($out);
                $out[] = ['n' => $n, 'sets' => $sets];
            }
        }
        return $out;
    }
}
